void drawCentered(const char* s, int16_t baseline_y,
                  uint16_t color, const GFXfont* font) {
    g_tft.setFont(font);
    g_tft.setTextColor(color);
    int16_t  x1, y1;
    uint16_t w, h;
    g_tft.getTextBounds(s, 0, 0, &x1, &y1, &w, &h);
    int16_t cx = (kScreenWidth - static_cast<int16_t>(w)) / 2;
    if (cx < 0) cx = 0;
    g_tft.setCursor(cx, baseline_y);
    g_tft.print(s);
}

// Compose the clock + date strings for the current moment from the host-
// supplied unix_ts + utc_offset, advancing via millis() since capture.
void composeIdleStrings(const QuotaSnapshot& q, uint64_t now_ms,
                        char* clock_out, size_t clock_sz,
                        char* date_out,  size_t date_sz) {
    if (q.unix_ts == 0) {
        std::strncpy(clock_out, "--:--", clock_sz);
        clock_out[clock_sz - 1] = '\0';
        std::strncpy(date_out, "set time via BLE", date_sz);
        date_out[date_sz - 1] = '\0';
        return;
    }

    uint32_t elapsed_s = 0;
    if (q.time_capture_ms > 0 && now_ms >= q.time_capture_ms) {
        elapsed_s = static_cast<uint32_t>(
            (now_ms - q.time_capture_ms) / 1000ULL);
    }
    time_t now_unix_utc   = static_cast<time_t>(q.unix_ts) + elapsed_s;
    time_t now_unix_local = now_unix_utc +
                            static_cast<time_t>(q.utc_offset_min) * 60;

    struct tm tm_buf;
    gmtime_r(&now_unix_local, &tm_buf);

    std::snprintf(clock_out, clock_sz, "%02d:%02d",
                  tm_buf.tm_hour, tm_buf.tm_min);

    static const char* kWday[] = {
        "Sun", "Mon", "Tue", "Wed", "Thu", "Fri", "Sat"};
    static const char* kMon[] = {
        "Jan", "Feb", "Mar", "Apr", "May", "Jun",
        "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"};
    int wday = tm_buf.tm_wday & 7;
    int mon  = tm_buf.tm_mon  % 12;
    if (mon < 0) mon = 0;
    std::snprintf(date_out, date_sz, "%s %s %d %d",
                  kWday[wday], kMon[mon], tm_buf.tm_mday,
                  1900 + tm_buf.tm_year);
}

// ASCII-art kitty in Claude orange. 7-character text rendered at
// textSize=2 (12×16 px per cell). Each mood has its own normal +
// blink frame so the cat actually expresses what Claude Code is doing
// to your quota.
struct PetSprite {
    const char* normal;
    const char* blink;
};
constexpr PetSprite kPetSprites[kPetMoodCount] = {
    /* HAPPY       */ {"(=^.^=)", "(=^_^=)"},
    /* CALM        */ {"(='.'=)", "(=-.-=)"},
    /* TIRED       */ {"(=u.u=)", "(=u_u=)"},
    /* STRESSED    */ {"(=>.<=)", "(=>_<=)"},
    /* OVERWHELMED */ {"(=T.T=)", "(=T_T=)"},
};

void drawPetFrameAt(int16_t cx, int16_t cy,
                    int8_t /*walk_frame*/, int8_t eye_frame,
                    int8_t mood) {
    if (mood < 0 || mood >= kPetMoodCount) mood = kPetMoodHappy;
    const PetSprite& sprites = kPetSprites[mood];
    const char* text = (eye_frame == 1) ? sprites.blink : sprites.normal;

    g_tft.setFont();                 // null → default 6×8 bitmap font
    g_tft.setTextSize(2);
    g_tft.setTextColor(kColorClaude);

    int16_t  x1, y1;
    uint16_t w, h;
    g_tft.getTextBounds(text, 0, 0, &x1, &y1, &w, &h);
    g_tft.setCursor(cx - static_cast<int16_t>(w) / 2,
                    cy - static_cast<int16_t>(h) / 2);
    g_tft.print(text);

    g_tft.setTextSize(1);            // restore
}

// Erase the sprite area at a given (cx, cy). Used when the cat moves so
// the trail behind it doesn't smear.
void erasePetAt(int16_t cx, int16_t cy) {
    if (cx < 0) return;
    g_tft.fillRect(cx - kIdlePetHalfW - 1,
                   kIdlePetTopY,
                   kIdlePetHalfW * 2 + 2,
                   kIdlePetBotY - kIdlePetTopY + 1,
                   kColorBg);
    (void)cy;
}

void drawIdleBody(const QuotaSnapshot& q, uint64_t now_ms,
                  bool force_full) {
    char clock_buf[8];
    char date_buf[24];
    composeIdleStrings(q, now_ms,
                       clock_buf, sizeof(clock_buf),
                       date_buf,  sizeof(date_buf));

    const bool full = force_full || !g_last_idle.initialized;
    if (full) {
        g_tft.fillScreen(kColorBg);
        // Reset cached pet position so the next displayTickPet repaints
        // immediately at the correct walk-phase coordinates.
        g_pet_last_x          = -1;
        g_pet_last_y          = -1;
        g_pet_frame_drawn     = -1;
        g_pet_last_walk_frame = -1;
        g_pet_last_mood       = -1;
        // Separator line under the date.
        g_tft.drawFastHLine(kRowMargin * 2, kIdleSeparatorY,
                            kScreenWidth - 4 * kRowMargin, kColorMuted);
    }

    if (full || std::strncmp(clock_buf, g_last_idle.clock_text,
                             sizeof(g_last_idle.clock_text)) != 0) {
        g_tft.fillRect(0, kIdleClockRowY, kScreenWidth, kIdleClockRowH,
                       kColorBg);
        drawCentered(clock_buf, kIdleClockBaselineY,
                     kColorFg, &FreeSansBold24pt7b);
        std::strncpy(g_last_idle.clock_text, clock_buf,
                     sizeof(g_last_idle.clock_text) - 1);
        g_last_idle.clock_text[sizeof(g_last_idle.clock_text) - 1] = '\0';
    }

    if (full || std::strncmp(date_buf, g_last_idle.date_text,
                             sizeof(g_last_idle.date_text)) != 0) {
        g_tft.fillRect(0, kIdleDateRowY, kScreenWidth, kIdleDateRowH,
                       kColorBg);
        drawCentered(date_buf, kIdleDateBaselineY,
                     kColorMuted, &FreeSans12pt7b);
        std::strncpy(g_last_idle.date_text, date_buf,
                     sizeof(g_last_idle.date_text) - 1);
        g_last_idle.date_text[sizeof(g_last_idle.date_text) - 1] = '\0';
    }

    if (full || std::strncmp(q.idle_status, g_last_idle.status_text,
                             sizeof(g_last_idle.status_text)) != 0) {
        g_tft.fillRect(0, kIdleStatusRowY, kScreenWidth, kIdleStatusRowH,
                       kColorBg);
        if (q.idle_status[0] != '\0') {
            drawCentered(q.idle_status, kIdleStatusBaselineY,
                         kColorGreen, &FreeSans12pt7b);
        }
        std::strncpy(g_last_idle.status_text, q.idle_status,
                     sizeof(g_last_idle.status_text) - 1);
        g_last_idle.status_text[sizeof(g_last_idle.status_text) - 1] = '\0';
    }

    g_last_idle.initialized = true;
}

void drawApiBody(uint32_t cost_micro_usd, uint32_t duration_s) {
    // Wipe the entire 5h+7d body region.
    g_tft.fillRect(0, k5hRowY,
                   kScreenWidth, (k7dRowY + k7dRowH) - k5hRowY,
                   kColorBg);

    // Cost: format as $X.XX (two decimals). 1_000_000 micro-USD = $1.00.
    char cost_buf[16];
    const unsigned dollars  = cost_micro_usd / 1000000u;
    const unsigned cents100 = (cost_micro_usd % 1000000u) / 10000u;  // 0..99
    std::snprintf(cost_buf, sizeof(cost_buf), "$%u.%02u", dollars, cents100);
    // Center horizontally, baseline a bit below the row midpoint.
    g_tft.setFont(&FreeSansBold24pt7b);
    int16_t  x1, y1;
    uint16_t w, h;
    g_tft.getTextBounds(cost_buf, 0, 0, &x1, &y1, &w, &h);
    const int16_t cx = (kScreenWidth - static_cast<int16_t>(w)) / 2;
    const int16_t cy = k5hRowY + 60;
    printAt(cost_buf, cx, cy, kColorGreen, &FreeSansBold24pt7b);

    // Duration label.
    char dur_buf[24];
    if (duration_s == 0) {
        std::strncpy(dur_buf, "", sizeof(dur_buf));
    } else {
        char tmp[16];
        formatDuration(duration_s, tmp, sizeof(tmp));
        std::snprintf(dur_buf, sizeof(dur_buf), "t: %s", tmp);
    }
    if (dur_buf[0] != '\0') {
        g_tft.setFont(&FreeSans9pt7b);
        g_tft.getTextBounds(dur_buf, 0, 0, &x1, &y1, &w, &h);
        const int16_t dx = (kScreenWidth - static_cast<int16_t>(w)) / 2;
        printAt(dur_buf, dx, k7dRowY + 50, kColorMuted, &FreeSans9pt7b);
    }
}

// ---------------------------------------------------------------- footer --
void drawFooter(const QuotaSnapshot& quota, uint64_t now_ms) {
    // Logo region: 40×40, paired with the 40×40 GIF on the far right.
    // Logo at X=156..195, GIF at X=200..240, 4-px gap between them.
    // Both centred on kStarCy=223 so they look balanced. The logo
    // overflows the footer top/bot by a few px into the surrounding
    // gap regions — that area is blank in the normal HUD layout.
    constexpr int16_t kFooterTextRightCap = 152;
    constexpr int16_t kFooterLogoX        = 156;
    constexpr int16_t kFooterLogoY        = kStarCy - kLogoH / 2;

    g_tft.fillRect(0, kFooterY, kFooterTextRightCap, kFooterH, kColorBg);
    // Wipe the logo's bbox before re-drawing so an OLD bigger logo can't
    // leave residue if we ever shrink it.
    g_tft.fillRect(kFooterLogoX, kFooterLogoY, kLogoW, kLogoH, kColorBg);
    g_tft.drawRGBBitmap(kFooterLogoX, kFooterLogoY,
                         const_cast<uint16_t*>(kLogo), kLogoW, kLogoH);

    char buf[40];
    uint16_t color;

    if (quota.last_update_ms == kTsUnset) {
        std::strncpy(buf, "waiting for data...", sizeof(buf));
        buf[sizeof(buf) - 1] = '\0';
        color = kColorMuted;
        g_last.footer_stale_min = -1;
        g_last.footer_stale_red = false;
    } else {
        const uint64_t delta_ms = (now_ms >= quota.last_update_ms)
                                      ? (now_ms - quota.last_update_ms)
                                      : 0;
        const uint32_t delta_min =
            static_cast<uint32_t>(delta_ms / 60000ULL);
        const bool stale = delta_min >= kStaleThresholdMin;
        if (stale) {
            std::snprintf(buf, sizeof(buf), "stale %lum",
                          static_cast<unsigned long>(delta_min));
            color = kColorRed;
        } else {
            std::snprintf(buf, sizeof(buf), "updated %lum ago",
                          static_cast<unsigned long>(delta_min));
            color = kColorMuted;
        }
        g_last.footer_stale_min = static_cast<int32_t>(delta_min);
        g_last.footer_stale_red = stale;
    }

    printAt(buf, kRowMargin, kFooterY + 18, color, &FreeSans9pt7b);
}

