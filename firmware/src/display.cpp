// =========================================================== public API ===
void displayInit() {
    pinMode(kPinLcdBl, OUTPUT);
    digitalWrite(kPinLcdBl, HIGH);

    // SCK, MISO (unused), MOSI, CS — explicit to leave nothing to library default.
    g_spi.begin(kPinLcdSclk, /*MISO=*/-1, kPinLcdMosi, kPinLcdCs);

    g_tft.init(kScreenWidth, kScreenHeight, SPI_MODE0);
    g_tft.setSPISpeed(40 * 1000 * 1000);  // 40 MHz — 4× former conservative
    g_tft.setRotation(0);
    g_tft.fillScreen(kColorBg);

    g_last = LastDrawnState{};
}

void displayRender(const DisplayView& view, bool full_redraw) {
    const QuotaSnapshot& q = view.quota;
    const uint8_t mode = q.mode;

    // ---- idle mode: takes over the whole screen ----
    if (mode == kSnapshotModeIdle) {
        // Mode flip from non-idle → idle: wipe everything once, then let
        // drawIdleBody's per-region diff cache take over.
        const bool flip_into_idle =
            (g_last.body_mode_drawn != -1) &&
            (g_last.body_mode_drawn != static_cast<int8_t>(mode));
        drawIdleBody(q, view.now_ms, full_redraw || flip_into_idle);
        if (flip_into_idle) {
            // Force HUD caches to be invalidated when we come back later.
            g_last = LastDrawnState{};
        }
        g_last.body_mode_drawn = static_cast<int8_t>(mode);
        return;
    }

    // Leaving idle mode (coming back into hud)? Reset idle cache and wipe.
    if (g_last.body_mode_drawn == static_cast<int8_t>(kSnapshotModeIdle)) {
        g_last_idle = LastDrawnIdle{};
        g_pet_last_x          = -1;
        g_pet_last_y          = -1;
        g_pet_frame_drawn     = -1;
        g_pet_last_walk_frame = -1;
        g_pet_last_mood       = -1;
        g_tft.fillScreen(kColorBg);
        g_last = LastDrawnState{};
        full_redraw = true;
    }

    // ---- header ----
    const bool title_changed = std::strncmp(g_last.title, q.title,
                                            sizeof(g_last.title)) != 0;
    if (full_redraw ||
        !g_last.header_drawn ||
        title_changed ||
        g_last.ble_connected != view.ble_connected) {
        drawHeader(q.title, view.ble_connected);
        g_last.header_drawn  = true;
        g_last.ble_connected = view.ble_connected;
        std::strncpy(g_last.title, q.title, sizeof(g_last.title) - 1);
        g_last.title[sizeof(g_last.title) - 1] = '\0';
    }

    // If we're switching modes, wipe both rows and reset their caches.
    const bool mode_flip = (g_last.body_mode_drawn != -1) &&
                           (g_last.body_mode_drawn != static_cast<int8_t>(mode));
    const bool body_full = full_redraw || mode_flip || g_last.body_mode_drawn == -1;
    if (mode_flip) {
        g_tft.fillRect(0, k5hRowY,
                       kScreenWidth, (k7dRowY + k7dRowH) - k5hRowY, kColorBg);
        g_last.pct_5h = -1; g_last.pct_7d = -1;
        g_last.used_5h = 0xFFFF; g_last.limit_5h = 0xFFFF;
        g_last.used_7d = 0xFFFF; g_last.limit_7d = 0xFFFF;
        g_last.reset_label_5h[0] = '\0';
        g_last.reset_label_7d[0] = '\0';
        g_last.cost_micro_usd = 0xFFFFFFFFu;
        g_last.duration_s     = 0xFFFFFFFFu;
    }

    if (mode == kSnapshotModeApi) {
        // ---- API body: cost + duration ----
        if (body_full ||
            g_last.cost_micro_usd != q.cost_micro_usd ||
            g_last.duration_s     != q.duration_s) {
            drawApiBody(q.cost_micro_usd, q.duration_s);
            g_last.cost_micro_usd = q.cost_micro_usd;
            g_last.duration_s     = q.duration_s;
        }
    } else {
        // ---- Subscription body: 5H + 7D rows with reset countdowns ----
        char reset_5h[16];
        char reset_7d[16];
        formatRemaining(
            liveRemainingS(q.reset_in_s_5h, q.last_update_ms, view.now_ms),
            reset_5h, sizeof(reset_5h));
        formatRemaining(
            liveRemainingS(q.reset_in_s_7d, q.last_update_ms, view.now_ms),
            reset_7d, sizeof(reset_7d));

        const uint8_t pct_5h = computePercent(q.used_5h, q.limit_5h);
        if (body_full ||
            g_last.pct_5h   != pct_5h ||
            g_last.used_5h  != q.used_5h ||
            g_last.limit_5h != q.limit_5h ||
            std::strncmp(g_last.reset_label_5h, reset_5h,
                         sizeof(g_last.reset_label_5h)) != 0) {
            drawRow("5H", reset_5h, k5hRowY, q.used_5h, q.limit_5h, pct_5h);
            g_last.pct_5h   = pct_5h;
            g_last.used_5h  = q.used_5h;
            g_last.limit_5h = q.limit_5h;
            std::strncpy(g_last.reset_label_5h, reset_5h,
                         sizeof(g_last.reset_label_5h) - 1);
            g_last.reset_label_5h[sizeof(g_last.reset_label_5h) - 1] = '\0';
        }

        const uint8_t pct_7d = computePercent(q.used_7d, q.limit_7d);
        if (body_full ||
            g_last.pct_7d   != pct_7d ||
            g_last.used_7d  != q.used_7d ||
            g_last.limit_7d != q.limit_7d ||
            std::strncmp(g_last.reset_label_7d, reset_7d,
                         sizeof(g_last.reset_label_7d)) != 0) {
            drawRow("7D", reset_7d, k7dRowY, q.used_7d, q.limit_7d, pct_7d);
            g_last.pct_7d   = pct_7d;
            g_last.used_7d  = q.used_7d;
            g_last.limit_7d = q.limit_7d;
            std::strncpy(g_last.reset_label_7d, reset_7d,
                         sizeof(g_last.reset_label_7d) - 1);
            g_last.reset_label_7d[sizeof(g_last.reset_label_7d) - 1] = '\0';
        }
    }

    g_last.body_mode_drawn = static_cast<int8_t>(mode);

    // ---- footer ----
    drawFooter(q, view.now_ms);
}

// Pet animation tick. Picks position + blink frame for the current
// millis() and repaints only when something changed. The cat walks
// across the top of the screen in a triangle-wave pattern, bobs Y
// every half-second to suggest stepping, and blinks every 4 seconds.
// Random-walk state: at random intervals we pick a new target X and
// smoothly lerp toward it. Gives the cat a wandering, less-mechanical
// feel than a triangle wave.
int16_t  g_pet_target_x       = (kIdlePetMinX + kIdlePetMaxX) / 2;
uint32_t g_pet_next_retarget  = 0;

// Per-mood movement profile. retarget interval shapes how often the
// cat picks a new destination, smoothing_divisor controls the glide
// speed (lower = faster glide). OVERWHELMED holds position.
struct PetMovementProfile {
    uint32_t retarget_min_ms;
    uint32_t retarget_max_ms;
    int16_t  smoothing_divisor;  // higher = slower glide
    bool     stops_in_centre;
};
constexpr PetMovementProfile kPetMovement[kPetMoodCount] = {
    /* HAPPY       */ {3500, 6500,  12, false},
    /* CALM        */ {3000, 6000,  14, false},
    /* TIRED       */ {4500, 8500,  20, false},
    /* STRESSED    */ {1200, 2500,   6, false},
    /* OVERWHELMED */ {99999, 99999, 1,  true },
};

// Claude splatter-star "thinking" loader (matches Claude iOS icon).
// Each ray has a fixed irregular base length + an independent slow
// shimmer (±12% length wobble, phase-offset per ray so they don't
// move in unison). The whole star rotates very slowly. Subtle — no
// dramatic pulsing, no "blinking" feel.
// Animated star: pick a pre-rendered RGB565 frame from kStarFrames[]
// based on now_ms and push it with one DMA SPI burst. Zero CPU spent
// on math each tick — the heavy lifting was done at build time.
constexpr uint32_t kStarFrameMs = 100;  // 100 ms × 8 frames = 0.8s/loop, fluid

void drawClaudeStar(uint64_t now_ms) {
    const int frame = static_cast<int>((now_ms / kStarFrameMs) % kStarFrameCount);
    g_tft.drawRGBBitmap(
        kStarCx - kStarFrameW / 2,
        kStarCy - kStarFrameH / 2,
        const_cast<uint16_t*>(kStarFrames[frame]),
        kStarFrameW, kStarFrameH);
}

void eraseClaudeStar() {
    g_tft.fillRect(kStarCx - kStarFrameW / 2,
                   kStarCy - kStarFrameH / 2,
                   kStarFrameW, kStarFrameH, kColorBg);
}

// Idle indicator — three horizontal dots, dim orange. Reads as "ready
// but at rest", like typing-dots that aren't actively typing. Replaces
// the spiky dim-splatter look that felt hostile.
void drawIdleDots() {
    constexpr int kDotR     = 3;
    constexpr int kDotGap   = 11;   // centre-to-centre spacing
    constexpr int kDotCount = 3;

    g_tft.fillRect(kStarCx - kIconW / 2, kStarCy - kIconH / 2,
                   kIconW, kIconH, kColorBg);

    const int total_w = (kDotCount - 1) * kDotGap;
    const int x0 = kStarCx - total_w / 2;
    for (int i = 0; i < kDotCount; ++i) {
        g_tft.fillCircle(x0 + i * kDotGap, kStarCy, kDotR, kColorClaudeDim);
    }
}

// Cache so we erase exactly once when thinking goes false.
bool g_star_was_drawn = false;

void displayTickPet(uint64_t now_ms, PetMood mood) {
    int8_t m = (mood < 0 || mood >= kPetMoodCount) ? kPetMoodHappy
                                                    : static_cast<int8_t>(mood);
    const PetMovementProfile& mv = kPetMovement[m];

    // Mood flipped → force a redraw and pick a fresh target.
    bool force_redraw = false;
    if (m != g_pet_last_mood) {
        g_pet_last_mood = m;
        g_pet_next_retarget = 0;  // retarget now
        force_redraw = true;
    }

    // OVERWHELMED stops in the centre (cat is too sad to walk).
    if (mv.stops_in_centre) {
        g_pet_target_x = (kIdlePetMinX + kIdlePetMaxX) / 2;
    } else if (static_cast<uint32_t>(now_ms) >= g_pet_next_retarget) {
        g_pet_target_x = static_cast<int16_t>(
            random(kIdlePetMinX, kIdlePetMaxX + 1));
        g_pet_next_retarget = static_cast<uint32_t>(now_ms) +
            static_cast<uint32_t>(random(mv.retarget_min_ms,
                                          mv.retarget_max_ms + 1));
    }

    // Smooth glide toward target.
    int16_t cx;
    if (g_pet_last_x < 0) {
        cx = g_pet_target_x;
    } else {
        const int32_t delta = g_pet_target_x - g_pet_last_x;
        cx = static_cast<int16_t>(
            g_pet_last_x + delta / mv.smoothing_divisor);
        if (cx == g_pet_last_x && delta != 0) {
            cx += (delta > 0) ? 1 : -1;
        }
    }
    if (cx < kIdlePetMinX) cx = kIdlePetMinX;
    if (cx > kIdlePetMaxX) cx = kIdlePetMaxX;

    // Bob Y — OVERWHELMED doesn't bob (sits sadly).
    const int16_t cy = (mv.stops_in_centre)
        ? kIdlePetBaseY
        : ((static_cast<uint32_t>(now_ms / kIdlePetBobPeriodMs) & 1)
            ? (kIdlePetBaseY + kIdlePetBobAmpY)
            : kIdlePetBaseY);

    // Blink.
    const uint32_t blink_phase =
        static_cast<uint32_t>(now_ms % kIdlePetBlinkPeriodMs);
    const int8_t eye_frame = (blink_phase >= (kIdlePetBlinkPeriodMs -
                                              kIdlePetBlinkDurMs)) ? 1 : 0;

    const bool same_y   = (cy == g_pet_last_y);
    const bool same_eye = (eye_frame == g_pet_frame_drawn);
    const int  dx       = (g_pet_last_x < 0) ? 9999 : (cx - g_pet_last_x);
    const int  abs_dx   = dx < 0 ? -dx : dx;
    if (!force_redraw && abs_dx < 2 && same_y && same_eye) {
        return;
    }

    erasePetAt(g_pet_last_x, g_pet_last_y);
    drawPetFrameAt(cx, cy, /*walk_frame=*/0, eye_frame, m);
    g_pet_last_x          = cx;
    g_pet_last_y          = cy;
    g_pet_frame_drawn     = eye_frame;
    g_pet_last_walk_frame = 0;
}

void displayTickStar(uint64_t now_ms, bool thinking) {
    if (thinking) {
        drawClaudeStar(now_ms);
        g_star_was_drawn = true;
    } else if (g_star_was_drawn) {
        eraseClaudeStar();
        g_star_was_drawn = false;
    }
}

// ─── App-state display (replaces the old thinking-only star) ──────────────
//
// Same 40×40 slot as the Claude splatter star. State machine:
//   idle      → erase, leave blank
//   thinking  → splatter star animation (reuses drawClaudeStar)
//   tool      → tool icon mapped from `detail` (longest-prefix match)
//   waiting   → pulsing orange dot
//
// Cached so unchanged ticks are cheap.
int8_t g_last_state_drawn    = kAppStateUnset;
char   g_last_state_detail[kAppStateDetailMaxLen + 1] = {0};
int8_t g_last_icon_idx       = -1;

// Find the longest-matching prefix in kIconMap for the given tool name.
// Returns -1 if no entry matches.
int8_t lookupIconForTool(const char* tool_name) {
    if (!tool_name || !*tool_name) return -1;
    int8_t best_idx  = -1;
    size_t best_len  = 0;
    for (int i = 0; i < kIconMapLen; ++i) {
        const char* p = kIconMap[i].prefix;
        const size_t plen = std::strlen(p);
        if (std::strncmp(tool_name, p, plen) == 0 && plen > best_len) {
            best_idx = kIconMap[i].icon_idx;
            best_len = plen;
        }
    }
    return best_idx;
}

void drawToolIcon(int8_t icon_idx) {
    if (icon_idx < 0 || icon_idx >= kIconCount) return;
    // Erase the slot first so previous content (idle/waiting/star) is gone.
    g_tft.fillRect(kStarCx - kIconW / 2, kStarCy - kIconH / 2,
                   kIconW, kIconH, kColorBg);
    g_tft.drawRGBBitmap(
        kStarCx - kIconW / 2,
        kStarCy - kIconH / 2,
        const_cast<uint16_t*>(kIcons[icon_idx]),
        kIconW, kIconH);
}

void drawWaitingPulse(uint64_t now_ms) {
    // Pulsing orange dot. 1.2 s period, radius 4..12 px.
    constexpr uint32_t kWaitPeriod = 1200;
    const float t = static_cast<float>(now_ms % kWaitPeriod) /
                    static_cast<float>(kWaitPeriod);
    const float pulse = (sinf(t * 6.28318530718f) + 1.0f) * 0.5f;
    const int r = 4 + static_cast<int>(pulse * 8);

    // Wipe the FULL 40×40 sprite slot — anything less leaves a halo of
    // the previous icon (Bash/MCP/etc.) visible around the dot.
    g_tft.fillRect(kStarCx - kIconW / 2, kStarCy - kIconH / 2,
                   kIconW, kIconH, kColorBg);
    g_tft.fillCircle(kStarCx, kStarCy, r, kColorClaude);
}

void displayTickState(uint64_t now_ms, AppState state, const char* detail) {
    if (state == kAppStateUnset) return;

    // Detect state transition or detail change.
    const bool state_changed = (state != g_last_state_drawn);
    const bool detail_changed =
        (state == kAppStateTool) &&
        (std::strncmp(detail, g_last_state_detail, kAppStateDetailMaxLen) != 0);

    switch (state) {
        case kAppStateIdle: {
            if (state_changed) {
                // Three dim dots — "ready, resting".
                drawIdleDots();
            }
            break;
        }
        case kAppStateThinking: {
            // Reuse the splatter star animation.
            drawClaudeStar(now_ms);
            g_star_was_drawn = true;
            break;
        }
        case kAppStateTool: {
            if (state_changed || detail_changed) {
                const int8_t idx = lookupIconForTool(detail);
                // Fall back to MCP icon (last entry) if no match.
                const int8_t draw_idx = (idx >= 0) ? idx : (kIconCount - 1);
                drawToolIcon(draw_idx);
                g_last_icon_idx = draw_idx;
                std::strncpy(g_last_state_detail, detail,
                             sizeof(g_last_state_detail) - 1);
                g_last_state_detail[sizeof(g_last_state_detail) - 1] = '\0';
            }
            break;
        }
        case kAppStateWaiting: {
            drawWaitingPulse(now_ms);
            break;
        }
        default: break;
    }

    g_last_state_drawn = state;
}

void displayTickFooter(const QuotaSnapshot& quota, uint64_t now_ms) {
    // Skip if the minute counter has not changed.
    if (quota.last_update_ms == kTsUnset) {
        if (g_last.footer_stale_min == -1) {
            return;
        }
    } else {
        const uint64_t delta_ms = (now_ms >= quota.last_update_ms)
                                      ? (now_ms - quota.last_update_ms)
                                      : 0;
        const uint32_t delta_min = static_cast<uint32_t>(delta_ms / 60000ULL);
        const bool     stale     = delta_min >= kStaleThresholdMin;
        if (static_cast<int32_t>(delta_min) == g_last.footer_stale_min &&
            stale == g_last.footer_stale_red) {
            return;
        }
    }
    drawFooter(quota, now_ms);
}

void displayUpdateConnection(bool connected) {
    if (g_last.header_drawn && g_last.ble_connected == connected) {
        return;
    }
    drawHeaderDot(connected);
    g_last.ble_connected = connected;
    g_last.header_drawn  = true;
}

void displayFlashAlert() {
    for (uint8_t i = 0; i < kAlertFlashCycles; ++i) {
        g_tft.fillScreen(kColorRed);
        delay(kAlertFlashIntervalMs);
        g_tft.fillScreen(kColorBg);
        delay(kAlertFlashIntervalMs);
    }
    // Invalidate the cache so the next displayRender repaints everything.
    g_last = LastDrawnState{};
}

// ============================================================ OTA mode ===
//
// While an OTA is in progress (the BLE OTA task is streaming firmware into
// the inactive flash slot) we take over the screen with a big progress
// readout. displayRender / displayTickFooter become no-ops via the
// `g_ota_active` flag so the HUD doesn't overpaint us.
//
// Layout (centered):
//   y=30   "OTA UPDATE" (bold 12pt)
//   y=110  "<PCT>%"     (bold 24pt, green)
//   y=160  progress bar (kBarHeight*2 tall)
//   y=210  "<received> / <total> B" (9pt muted)

namespace {
volatile bool g_ota_active   = false;
int16_t       g_ota_last_pct = -1;

void drawOtaFrame(uint32_t received, uint32_t total, int16_t pct) {
    char buf[40];

    // Header bar.
    g_tft.fillRect(0, 0, kScreenWidth, 50, kColorBg);
    printAt("OTA UPDATE",
            kRowMargin, 30, kColorFg, &FreeSansBold12pt7b);

    // Big percentage, centered.
    std::snprintf(buf, sizeof(buf), "%d%%", static_cast<int>(pct));
    g_tft.setFont(&FreeSansBold24pt7b);
    int16_t  x1, y1;
    uint16_t w, h;
    g_tft.getTextBounds(buf, 0, 0, &x1, &y1, &w, &h);
    g_tft.fillRect(0, 80, kScreenWidth, 50, kColorBg);
    g_tft.setCursor((kScreenWidth - static_cast<int16_t>(w)) / 2, 120);
    g_tft.setTextColor(kColorGreen);
    g_tft.print(buf);

    // Progress bar.
    const int16_t bar_x = 20;
    const int16_t bar_y = 160;
    const int16_t bar_w = kScreenWidth - 40;
    const int16_t bar_h = kBarHeight * 2;
    g_tft.fillRect(bar_x, bar_y, bar_w, bar_h, kColorBarTrack);
    const int16_t fill_w =
        static_cast<int16_t>((static_cast<int32_t>(bar_w) * pct) / 100);
    if (fill_w > 0) {
        g_tft.fillRect(bar_x, bar_y, fill_w, bar_h, kColorGreen);
    }
    g_tft.drawRect(bar_x, bar_y, bar_w, bar_h, kColorMuted);

    // Bytes counter.
    std::snprintf(buf, sizeof(buf), "%u / %u B",
                  static_cast<unsigned>(received),
                  static_cast<unsigned>(total));
    g_tft.fillRect(0, 200, kScreenWidth, 30, kColorBg);
    g_tft.setFont(&FreeSans9pt7b);
    g_tft.getTextBounds(buf, 0, 0, &x1, &y1, &w, &h);
    g_tft.setCursor((kScreenWidth - static_cast<int16_t>(w)) / 2, 220);
    g_tft.setTextColor(kColorMuted);
    g_tft.print(buf);
}
}  // anon namespace

void displayBeginOta() {
    g_ota_active   = true;
    g_ota_last_pct = -1;
    g_tft.fillScreen(kColorBg);
    drawOtaFrame(0, 1, 0);
}

void displayOtaProgress(uint32_t received, uint32_t total) {
    if (!g_ota_active) {
        // First call without an explicit begin — set up the screen first.
        displayBeginOta();
    }
    if (total == 0) return;
    int16_t pct = static_cast<int16_t>(
        (static_cast<uint64_t>(received) * 100ULL) / total);
    if (pct > 100) pct = 100;
    if (pct == g_ota_last_pct) {
        return;  // cheap path: nothing to redraw at this granularity
    }
    g_ota_last_pct = pct;
    drawOtaFrame(received, total, pct);
}

bool displayIsOtaActive() {
    return g_ota_active;
}

